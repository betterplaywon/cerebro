/**
 * PIPA 경계 가드 — 수집 데이터(특히 커뮤니티 UGC)와 LLM 산출물에서 명백한 민감
 * 식별자를 그래프(저장·표시)·외부 전송 전에 마스킹한다.
 *
 * 왜: 블로그/카페/지식인 등 사용자생성콘텐츠(UGC)는 개인 연락처·식별번호가
 * 섞일 위험이 웹/뉴스보다 높다. "공개정보·공인 한정" 정책을 보강하는
 * best-effort 방어이며, 완전한 PII 탐지를 대체하지 않는다.
 * 과탐(예: 임의 13~16자리 코드·제품번호·기사ID, 공개된 대표 유선번호)을 피하려고
 * **구조가 명백한 패턴만**(날짜부 검증·Luhn 검증) 처리한다.
 *
 * 범위 밖(정규식으로 잡지 않음): 주소·금융상세·건강·정치/종교/성적지향 등
 * 자유서술형 민감정보. 이는 "공인·공개정보 한정" 정책 + LLM 프롬프트 가드
 * (report.ts SYSTEM_PROMPT)에 위임한다. coverage를 과대표현하지 말 것.
 */

/**
 * 주민등록번호 + 외국인등록번호: YYMMDD(유효 월·일) + 성별코드 + 6자리.
 * 성별코드 1~4=내국인, 5~8=외국인(2000년대 출생 7·8 포함). 구분자(하이픈/점/공백) 선택 —
 * 한국어 텍스트가 날짜부를 점으로 쓰는 습관(2024.01.01)이 주민번호 표기로도 이어져 `.`도 허용.
 * 날짜부(MM 01~12·DD 01~31) 검증으로 임의 13자리 숫자열 오탐을 줄인다.
 * 트레이드오프: 법인등록번호도 6+7 구조라 날짜형 접두(예 110111-…)면 함께 마스킹될 수 있다 —
 * 주민번호 누출(골든룰)보다 공개 법인번호 과(過)마스킹이 안전하므로 의도적으로 허용한다.
 */
const NATIONAL_ID = /\b\d{2}(?:0[1-9]|1[0-2])(?:0[1-9]|[12]\d|3[01])[-.\s]?[1-8]\d{6}\b/g;
/** 휴대전화: 010/011/016~019 + 3~4자리 + 4자리. (개인 식별 위험이 큰 휴대번호만 — 유선/대표번호 제외) */
const MOBILE = /\b01[016789][-.\s]?\d{3,4}[-.\s]?\d{4}\b/g;
/**
 * 이메일(연락처). 로컬/도메인 라벨 길이를 RFC 상한(64/63)으로 제한해 긴 비(非)이메일 입력에서의
 * 2차(O(n²)) 백트래킹을 차단한다(redactSensitive는 무제한 길이 외부 스니펫·LLM 출력에 적용됨).
 * 도메인에 최소 1개의 점 요구, 트레일링 구두점 미포함.
 */
const EMAIL = /[\w.+-]{1,64}@[\w-]{1,63}(?:\.[\w-]{1,63})+/g;
/** 카드번호 후보: 13~19자리(그룹 사이 단일 공백/하이픈 허용). **Luhn 통과 시에만** 마스킹(오탐 저감). */
const CARD_CANDIDATE = /\b\d(?:[ -]?\d){12,18}\b/g;

const MASK = '●●●';

/**
 * Luhn 체크섬 검증(카드번호 진위 추정). 임의 숫자열·제품코드·긴 기사ID의 오탐을 줄인다.
 * 순수 함수 — 외부 의존성 없이 ~10줄(YAGNI: 라이브러리 불필요).
 */
function passesLuhn(digits: string): boolean {
  let sum = 0;
  let double = false;
  for (let i = digits.length - 1; i >= 0; i -= 1) {
    const d = digits.charCodeAt(i) - 48; // '0'(48)~'9'(57)
    if (d < 0 || d > 9) return false;
    let v = d;
    if (double) {
      v *= 2;
      if (v > 9) v -= 9;
    }
    sum += v;
    double = !double;
  }
  return sum % 10 === 0;
}

/**
 * 민감 식별자(주민·외국인등록번호·휴대전화·이메일·신용카드)를 마스킹한 문자열을 반환한다.
 * 순서 주의: 식별번호·전화·이메일을 먼저 마스킹해 카드 후보 단계의 중복 매칭을 줄인다.
 * 마스킹 토큰('●●●')은 normalize.tokenize의 분리 문자라 토픽 노이즈가 되지 않는다.
 */
export function redactSensitive(text: string): string {
  return text
    .replace(NATIONAL_ID, MASK)
    .replace(MOBILE, MASK)
    .replace(EMAIL, MASK)
    .replace(CARD_CANDIDATE, (m) => (passesLuhn(m.replace(/[ -]/g, '')) ? MASK : m));
}
