/**
 * PIPA 경계 가드 — 수집 데이터(특히 커뮤니티 UGC)에서 명백한 민감 식별자를
 * 그래프(저장·표시) 유입 전에 마스킹한다.
 *
 * 왜: 블로그/카페/지식인 등 사용자생성콘텐츠(UGC)는 개인 연락처·식별번호가
 * 섞일 위험이 웹/뉴스보다 높다. "공개정보·공인 한정" 정책을 보강하는
 * best-effort 방어이며, 완전한 PII 탐지를 대체하지 않는다.
 * 과탐(예: 정상 13자리 코드, 공개된 대표번호)을 피하려고 **명백한 패턴만** 처리한다.
 */

/** 주민등록번호: 6자리 + 성별코드(1~4) + 6자리. 구분자(하이픈/공백) 선택. */
const RRN = /\b\d{6}[-\s]?[1-4]\d{6}\b/g;
/** 휴대전화: 010/011/016~019 + 3~4자리 + 4자리. (개인 식별 위험이 큰 휴대번호만) */
const MOBILE = /\b01[016789][-.\s]?\d{3,4}[-.\s]?\d{4}\b/g;

const MASK = '●●●';

/** 민감 식별자(주민번호·휴대전화)를 마스킹한 문자열을 반환한다. */
export function redactSensitive(text: string): string {
  return text.replace(RRN, MASK).replace(MOBILE, MASK);
}
